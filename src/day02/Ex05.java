package day02;

public class Ex05 {
public static void main(String[] args) {
	final String KOREA ="대한민국";
//	final은 상수화 시키는 별칭으로
//	final이 붙은 변수는 다른 값을 대입할 수 없다.
//	final로 지정한 변수값은 될 수 있으면 대문자로 사용
	System.out.println(KOREA);
//	korea = "미국";
//	이렇게 위에 final이 있기 떄문에 변수에 오류가 생긴다.
	System.out.println(KOREA);
	System.out.println(KOREA);
//	ctrl+alt+방향키를 누르면 그 문장 복사 후 방향키 방향으로
//	이동하며
//	alt방향키로 위 아래로 이동이 가능하다.
}
}
